import UIKit

extension Cerise where Base: UIColor {
    static var tint: UIColor {
        return UIColor(named: "TINT") ?? UIColor(red: 222, green: 49, blue: 99, alpha: 1)
    }

    static var title: UIColor {
        return UIColor(named: "BK80")!
    }
}

